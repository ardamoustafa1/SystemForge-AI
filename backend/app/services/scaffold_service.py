"""
Generate a downloadable project scaffold ZIP from a design's AI output.

Reads the design input (preferred_stack, deployment_scope) and output
(core_components, recommended_implementation_phases) to produce an
in-memory ZIP archive containing ready-to-run starter files.
"""

import io
import re
import zipfile
from textwrap import dedent

from app.schemas.design import DesignInputPayload, DesignOutputPayload


# ─── Stack Detection ────────────────────────────────────────────
def _detect(stack: str, keywords: list[str]) -> bool:
    low = stack.lower()
    return any(kw in low for kw in keywords)


# ─── Template Builders ──────────────────────────────────────────
def _readme(title: str, services: list[str]) -> str:
    svc_list = "\n".join(f"- {s}" for s in services)
    return dedent(f"""\
        # {title}

        > Auto-generated scaffold by **SystemForge AI**.

        ## Services

        {svc_list}

        ## Getting Started

        ```bash
        docker compose up --build
        ```

        Then open the application in your browser.
    """)


def _env_example(flags: dict[str, bool]) -> str:
    lines = ["# Auto-generated by SystemForge AI", ""]
    if flags.get("postgres"):
        lines += ["POSTGRES_USER=app", "POSTGRES_PASSWORD=changeme", "POSTGRES_DB=app_db", "DATABASE_URL=postgresql://app:changeme@postgres:5432/app_db", ""]
    if flags.get("mongo"):
        lines += ["MONGO_URL=mongodb://mongo:27017/app_db", ""]
    if flags.get("redis"):
        lines += ["REDIS_URL=redis://redis:6379/0", ""]
    lines += ["NODE_ENV=development", ""]
    return "\n".join(lines)


def _docker_compose(flags: dict[str, bool], title: str) -> str:
    services: dict[str, str] = {}

    if flags.get("fastapi"):
        services["backend"] = dedent("""\
          backend:
            build: ./backend
            ports:
              - "8000:8000"
            env_file: .env
            depends_on:
              - {deps}
            volumes:
              - ./backend:/app
            command: uvicorn app.main:app --host 0.0.0.0 --port 8000 --reload
        """)
    elif flags.get("django"):
        services["backend"] = dedent("""\
          backend:
            build: ./backend
            ports:
              - "8000:8000"
            env_file: .env
            depends_on:
              - {deps}
            volumes:
              - ./backend:/app
            command: python manage.py runserver 0.0.0.0:8000
        """)
    elif flags.get("nestjs"):
        services["backend"] = dedent("""\
          backend:
            build: ./backend
            ports:
              - "3001:3001"
            env_file: .env
            depends_on:
              - {deps}
            volumes:
              - ./backend:/app
            command: npm run start:dev
        """)

    if flags.get("nextjs"):
        services["frontend"] = dedent("""\
          frontend:
            build: ./frontend
            ports:
              - "3000:3000"
            env_file: .env
            volumes:
              - ./frontend:/app
            command: npm run dev
        """)
    elif flags.get("vite"):
        services["frontend"] = dedent("""\
          frontend:
            build: ./frontend
            ports:
              - "5173:5173"
            env_file: .env
            volumes:
              - ./frontend:/app
            command: npm run dev -- --host 0.0.0.0
        """)

    if flags.get("postgres"):
        services["postgres"] = dedent("""\
          postgres:
            image: postgres:16-alpine
            environment:
              POSTGRES_USER: app
              POSTGRES_PASSWORD: changeme
              POSTGRES_DB: app_db
            ports:
              - "5432:5432"
            volumes:
              - pgdata:/var/lib/postgresql/data
        """)

    if flags.get("redis"):
        services["redis"] = dedent("""\
          redis:
            image: redis:7-alpine
            ports:
              - "6379:6379"
        """)

    if flags.get("mongo"):
        services["mongo"] = dedent("""\
          mongo:
            image: mongo:7
            ports:
              - "27017:27017"
            volumes:
              - mongodata:/data/db
        """)

    # Build deps list for backend
    deps_list = []
    if flags.get("postgres"):
        deps_list.append("postgres")
    if flags.get("redis"):
        deps_list.append("redis")
    if flags.get("mongo"):
        deps_list.append("mongo")
    deps_str = "\n              - ".join(deps_list) if deps_list else "[]"

    body = ""
    for svc_yaml in services.values():
        body += svc_yaml.replace("{deps}", deps_str)

    volumes_section = ""
    vol_names: list[str] = []
    if flags.get("postgres"):
        vol_names.append("pgdata:")
    if flags.get("mongo"):
        vol_names.append("mongodata:")
    if vol_names:
        volumes_section = "\nvolumes:\n" + "\n".join(f"  {v}" for v in vol_names) + "\n"

    return dedent(f"""\
        # {title} — Docker Compose
        # Auto-generated by SystemForge AI

        services:
        {body}{volumes_section}""")


# ─── Framework Stubs ────────────────────────────────────────────
def _fastapi_files(zf: zipfile.ZipFile) -> None:
    zf.writestr("backend/Dockerfile", dedent("""\
        FROM python:3.12-slim
        WORKDIR /app
        COPY requirements.txt .
        RUN pip install --no-cache-dir -r requirements.txt
        COPY . .
        CMD ["uvicorn", "app.main:app", "--host", "0.0.0.0", "--port", "8000"]
    """))
    zf.writestr("backend/requirements.txt", dedent("""\
        fastapi>=0.115
        uvicorn[standard]>=0.30
        sqlalchemy>=2.0
        alembic>=1.13
        psycopg2-binary>=2.9
        pydantic>=2.7
        python-dotenv>=1.0
    """))
    zf.writestr("backend/app/__init__.py", "")
    zf.writestr("backend/app/main.py", dedent("""\
        from fastapi import FastAPI
        from fastapi.middleware.cors import CORSMiddleware

        app = FastAPI(title="API", version="0.1.0")

        app.add_middleware(
            CORSMiddleware,
            allow_origins=["*"],
            allow_credentials=True,
            allow_methods=["*"],
            allow_headers=["*"],
        )


        @app.get("/health")
        def health():
            return {"status": "ok"}
    """))


