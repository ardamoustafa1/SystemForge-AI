# ─── Main Entry ─────────────────────────────────────────────────
def build_scaffold_zip(
    title: str,
    design_input: DesignInputPayload,
    design_output: DesignOutputPayload,
) -> bytes:
    """Return in-memory ZIP bytes for the project scaffold."""

    stack = (design_input.preferred_stack or "") + " " + " ".join(design_output.core_components)

    flags: dict[str, bool] = {
        "fastapi": _detect(stack, ["fastapi", "fast api"]),
        "django": _detect(stack, ["django"]),
        "nestjs": _detect(stack, ["nestjs", "nest.js", "nest js"]),
        "nextjs": _detect(stack, ["nextjs", "next.js", "next js"]),
        "vite": _detect(stack, ["vite", "react"]) and not _detect(stack, ["nextjs", "next.js", "next js"]),
        "postgres": _detect(stack, ["postgres", "postgresql", "pg"]),
        "redis": _detect(stack, ["redis"]),
        "mongo": _detect(stack, ["mongo", "mongodb"]),
    }

    # Ensure at least one backend + frontend
    if not any(flags.get(k) for k in ["fastapi", "django", "nestjs"]):
        flags["fastapi"] = True  # default backend
    if not any(flags.get(k) for k in ["nextjs", "vite"]):
        flags["nextjs"] = True  # default frontend
    if not any(flags.get(k) for k in ["postgres", "mongo"]):
        flags["postgres"] = True  # default database

    service_names = [k for k, v in flags.items() if v]

    buf = io.BytesIO()
    with zipfile.ZipFile(buf, "w", zipfile.ZIP_DEFLATED) as zf:
        slug = re.sub(r"[^a-z0-9]+", "-", title.lower()).strip("-")[:48] or "project"  # type: ignore

        zf.writestr(f"{slug}/README.md", _readme(title, service_names))
        zf.writestr(f"{slug}/.env.example", _env_example(flags))
        zf.writestr(f"{slug}/docker-compose.yml", _docker_compose(flags, title))
        zf.writestr(f"{slug}/.gitignore", "node_modules/\n__pycache__/\n.env\n*.pyc\n.next/\ndist/\n")

        # Write framework files with slug prefix
        inner_buf = io.BytesIO()
        with zipfile.ZipFile(inner_buf, "w", zipfile.ZIP_DEFLATED) as inner_zf:
            if flags.get("fastapi"):
                _fastapi_files(inner_zf)
            elif flags.get("django"):
                _django_files(inner_zf)
            elif flags.get("nestjs"):
                _nestjs_files(inner_zf)

            if flags.get("nextjs"):
                _nextjs_files(inner_zf)
            elif flags.get("vite"):
                _vite_files(inner_zf)

        inner_buf.seek(0)
        with zipfile.ZipFile(inner_buf, "r") as inner_zf:
            for item in inner_zf.infolist():
                zf.writestr(f"{slug}/{item.filename}", inner_zf.read(item.filename))

    return buf.getvalue()
